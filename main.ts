scene.onOverlapTile(SpriteKind.Player, myTiles.tile9, function (sprite, location) {
    if (sprite.tileKindAt(TileDirection.Bottom, myTiles.tile9)) {
        tiles.setTileAt(location, myTiles.tile1)
        info.changeScoreBy(1)
        sprite.vy = -150
        music.jumpUp.play()
    } else {
        tiles.setTileAt(location, myTiles.tile1)
        info.changeLifeBy(-1)
        music.jumpDown.play()
    }
})
